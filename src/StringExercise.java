public class StringExercise {
    public static void main(String[] args){
        String first = "We don't need no education\n" + "We don't need no thought control";
        System.out.printf("%s%n", first);

        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.printf("%s%n", check);

        String next = "In windows, the main drive is usually C:\\";
        System.out.println(next);

        String slash = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.printf("%s%n", slash);
    }
}
